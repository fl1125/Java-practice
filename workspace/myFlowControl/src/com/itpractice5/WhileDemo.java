package com.itpractice5;
/*
 * while循坏的语句格式：
 *     while(判断条件句){
 *     循坏体语句;
 *     }
 * 扩展格式：
 *     初始化语句；
 *       while(判断条件语句){
 *         循坏体语句;
 *         控制体语句;
 *         }
 * 
 * 对比for循环：
 *    for(初始化语句;判断条件语句;控制条件语句;){
 *    循环体语句；
 *    }
 * 
 */
public class WhileDemo {

	public static void main(String[] args) {
	    //在控制台输出10次HelloWorld
		int x = 0;
		while(x < 10){
			System.out.println("HelloWorld");
			x++;
		}
	}

}
