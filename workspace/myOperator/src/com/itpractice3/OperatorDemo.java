package com.itpractice3;
/*
 *  关系运算符：
 *    ==,!=,>,>=,<,<=
 *    用来比较两个数据的大小，关系运算符的结果是boolean类型的
 * 
 * 
 * 
 * 
 * 
 * 
 */
public class OperatorDemo {

	public static void main(String[] args) {
     int a = 10;
     int b = 20;
     int c = 10;
     System.out.println(a == b);
     System.out.println(a == c);
	}

}
